$limit = (Get-Date).AddDays(-365)
$path = "D:\path"

get-date

# Delete files older than the $limit.
Get-ChildItem -Path $path -Recurse -Force | Where-Object { !$_.PSIsContainer -and $_.CreationTime -lt $limit } | Remove-Item -Force

get-date

